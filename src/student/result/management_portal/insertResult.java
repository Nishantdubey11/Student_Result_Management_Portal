    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String em = jTextField2.getText();
        String dbms = jTextField3.getText();
        String os = jTextField4.getText();
        String maths = jTextField5.getText();
        String ml = jTextField6.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "Root@123");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+em+"','"+dbms+"','"+os+"','"+maths+"','"+ml+"')");
                JOptionPane.showMessageDialog(null, "Data updated successfully!");
                setVisible(false);
                new insertResult().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Roll number does not !");
            }
        }
        catch(Exception e){

            JOptionPane.showMessageDialog(null, e.toString());

        }

    }//GEN-LAST:event_saveBtnActionPerformed

